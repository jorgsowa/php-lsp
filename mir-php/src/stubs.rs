/// Built-in PHP function and class stubs.
///
/// Provides arities for core PHP functions and common extensions so the
/// analyser does not flag standard-library calls as undefined.
///
/// Each entry is `(name, required_args, max_args)`.
/// `usize::MAX` means variadic (no upper bound).

/// Arity bounds for a built-in callable.
#[derive(Debug, Clone, Copy)]
pub struct Arity {
    pub required: usize,
    pub max: usize,
}

/// Look up the arity of a built-in PHP function by name.
/// Returns `None` if the name is not a known built-in.
pub fn function_arity(name: &str) -> Option<Arity> {
    BUILTIN_FUNCTIONS
        .binary_search_by_key(&name, |&(n, _, _)| n)
        .ok()
        .map(|i| {
            let (_, req, max) = BUILTIN_FUNCTIONS[i];
            Arity { required: req, max }
        })
}

/// True if `name` is a known built-in class / interface / exception.
pub fn is_builtin_class(name: &str) -> bool {
    BUILTIN_CLASSES
        .binary_search_by_key(&name, |&n| n)
        .is_ok()
}

