#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn builtin_functions_are_sorted() {
        for pair in BUILTIN_FUNCTIONS.windows(2) {
            assert!(
                pair[0].0 <= pair[1].0,
                "out of order: '{}' before '{}'",
                pair[0].0,
                pair[1].0
            );
        }
    }

    #[test]
    fn builtin_classes_are_sorted() {
        for pair in BUILTIN_CLASSES.windows(2) {
            assert!(pair[0] <= pair[1], "out of order: '{}' before '{}'", pair[0], pair[1]);
        }
    }

    #[test]
    fn strlen_is_known() {
        assert!(function_arity("strlen").is_some());
    }

    #[test]
    fn pdo_is_builtin_class() {
        assert!(is_builtin_class("PDO"));
    }

    #[test]
    fn unknown_function_is_none() {
        assert!(function_arity("totally_unknown_fn_xyz").is_none());
    }
}
